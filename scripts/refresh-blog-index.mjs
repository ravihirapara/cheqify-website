#!/usr/bin/env node
import { writeFileSync, readFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const PROJECT_ID = "4lvsthqk";
const DATASET = "production";
const QUERY = `*[_type=="blogPost"]{order, "slug": slug.current, title_en, publishedAt, isHowTo, hasFaq, tags} | order(order asc)`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const indexPath = resolve(__dirname, "..", "_bmad-output", "blog-content", "PUBLISHED-INDEX.md");

const url = new URL(`https://${PROJECT_ID}.api.sanity.io/v2023-10-01/data/query/${DATASET}`);
url.searchParams.set("query", QUERY);

const res = await fetch(url);
if (!res.ok) {
  console.error(`Sanity query failed: ${res.status} ${res.statusText}`);
  process.exit(1);
}
const { result } = await res.json();
if (!Array.isArray(result) || result.length === 0) {
  console.error("No posts returned from Sanity.");
  process.exit(1);
}

const today = new Date().toISOString().slice(0, 10);

const liveRows = result
  .map((p, i) => {
    const tags = Array.isArray(p.tags) ? p.tags.join(", ") : "";
    const flags = [p.isHowTo && "HowTo", p.hasFaq && "FAQ"].filter(Boolean).join(" + ") || "—";
    const title = (p.title_en || "").replace(/\|/g, "\\|");
    return `| ${i + 1} | ${p.order ?? ""} | \`${p.slug}\` | ${title} | ${tags} | ${flags} |`;
  })
  .join("\n");

const liveSlugs = new Set(result.map((p) => p.slug));

const existing = readFileSync(indexPath, "utf8");

// Section headers to preserve (manual edits). Order matters — must match output below.
const SECTIONS = [
  "## Drafted on Disk — Pending Publish",
  "## Pending Topics — Not Yet Drafted",
  "## Topics Already Covered — Do NOT Duplicate",
];
const TRAILING = "## How to Refresh This Index";

function extractSection(text, header, nextHeader) {
  const start = text.indexOf(header);
  if (start < 0) return "";
  const end = text.indexOf(nextHeader, start + header.length);
  const raw = end < 0 ? text.slice(start) : text.slice(start, end);
  // Strip trailing horizontal rules + whitespace so the writer's own `---` separators don't double up
  return raw.replace(/(\s*-{3,}\s*)+$/g, "").trim();
}

const draftedOnDisk = extractSection(existing, SECTIONS[0], SECTIONS[1])
  || `${SECTIONS[0]}\n\n_(none — all drafts published)_`;
const pendingTopics = extractSection(existing, SECTIONS[1], SECTIONS[2])
  || `${SECTIONS[1]}\n\n_(populate manually — pick from SEO-PLAN.md Phase 5B or propose new high-intent topics)_`;
const alreadyCovered = extractSection(existing, SECTIONS[2], TRAILING)
  || `${SECTIONS[2]}\n\n_(none yet)_`;

const refreshSection = `## How to Refresh This Index

Run from project root:

\`\`\`bash
node scripts/refresh-blog-index.mjs
\`\`\`

This regenerates the "Live Posts" table directly from Sanity. The three manual sections below it ("Drafted on Disk", "Pending Topics", "Topics Already Covered") are preserved across runs — edit them by hand.

---

## Process Reminder

**Before drafting any new post:**
1. Run \`node scripts/refresh-blog-index.mjs\` to sync.
2. Check "Drafted on Disk" — if a slot already has a draft, paste+publish that instead of writing new.
3. Pick a slug from "Pending Topics", OR propose a new one and verify against "Topics Already Covered".
4. Slug must not exist in "Live Posts" above.
5. Draft following the format of \`post-12-cheque-validity-stop-payment.md\`.
6. Move the entry from "Pending Topics" → "Drafted on Disk" in this file.
7. After publishing in Sanity, re-run the refresh script (auto-removes from "Drafted on Disk" if you also update it manually).
`;

const out = `# Published Blog Posts — Master Index

**Last synced from Sanity:** ${today}
**Source:** \`*[_type=="blogPost"] | order(order asc)\` against project \`${PROJECT_ID}\` / dataset \`${DATASET}\`

> **This file is auto-generated by \`scripts/refresh-blog-index.mjs\`.** The "Live Posts" table is overwritten on every run. The three sections below it are preserved and must be edited by hand.

---

## Live Posts (${result.length})

| # | Order | Slug | Title (EN) | Tags | Type |
|---|---|---|---|---|---|
${liveRows}

---

${draftedOnDisk}

---

${pendingTopics}

---

${alreadyCovered}

---

${refreshSection}
`;

writeFileSync(indexPath, out);

const liveSummary = result.map((p) => `${p.order ?? "?"}. ${p.slug}`).join("\n  ");
console.log(`Wrote ${indexPath}`);
console.log(`${result.length} posts synced:\n  ${liveSummary}`);
console.log("\nSlugs now live (use this to filter backlog):");
console.log(JSON.stringify([...liveSlugs], null, 2));
